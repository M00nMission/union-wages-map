// Helper function to get next available local number
export const getNextAvailableLocalNumber = (): number => {
  const existingNumbers = getAllIBEWLocalNumbers().sort((a, b) => a - b)
  if (existingNumbers.length === 0) return 1
  
  // Find the first gap in the sequence
  for (let i = 0; i < existingNumbers.length; i++) {
    if (existingNumbers[i] !== i + 1) {
      return i + 1
    }
  }
  
  // If no gaps, return the next number after the highest
  return Math.max(...existingNumbers) + 1
} 
